#include <stdio.h>

void ft_swap(int *a, int *b)
{
	int tmp = *a;
	*a = *b;
	*b = tmp;
}

int main()
{
	int A = 1;
	int B = 2;
	int *a = &A;
	int *b = &B;


	ft_swap(a, b);

	printf("a = %d \n b = %d", *a, *b);
}
