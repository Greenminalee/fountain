#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	beg;
	int		fn;
	int		tmp;

	fn = 1000000000;
	while (fn > 0)
	{
		beg = nb / fn + 48;
		if (beg != '0')
		{
			write(1, &beg, 1);
		}
		nb %= fn;
		fn /= 10;
	}
}

int		main(void)
{
	ft_putnbr(111314131);
	return (0);
}
